#include<stdio.h>
#include<math.h>

struct euclid
{
    float x,y;
};

void main()
{
    float d;
    struct euclid pt1,pt2;
    printf("input the coordinates of point 1:");
    scanf("%f %f",&pt1.x,&pt1.y);
    printf("input the coordinates of point 2: ");
    scanf("%f %f",&pt2.x,&pt2.y);
    d=sqrt((pow(pt2.x-pt1.x,2))+(pow(pt2.y-pt1.y,2)));
    printf("The distance between the two points= %.2f",d);
}



















































































































































































































































































